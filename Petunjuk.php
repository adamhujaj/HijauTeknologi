cara penggunaan aplikasi 
1.nyalakan xampp
2.expor db hijauTeknologi ke database
3.ketik php artisan serve diterminal cmd
4.setelah muncul alamat ip tambahkan "/"
http://127.0.0.1:8000/login untuk mengakses halaman login
http://127.0.0.1:8000/owner untuk mengakses halaman owner
http://127.0.0.1:8000/staff untuk mengakses halaman Staff
http://127.0.0.1:8000/crud untuk mengakses halaman crud

5. aplikasi ini belum dilengkapi fitur role dan permission
